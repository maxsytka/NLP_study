# Покращена функція токенізації
def simple_tokenize(text):
    # Видаляємо зайві пробіли
    text = re.sub(r'\s+', ' ', text).strip()
    # Обробляємо пунктуацію, додаючи пробіли до/після символів
    text = re.sub(r'([.,!?;:])', r' \1 ', text)
    # Видаляємо зайві пробіли, які могли з'явитися
    text = re.sub(r'\s+', ' ', text).strip()
    # Розділяємо за пробілами
    return text.split()

# Покращена функція передобробки тексту
def preprocess_text(text, language):
    """Покращена передобробка текстових даних"""
    # Переводимо до нижнього регістру
    text = text.lower()

    # Нормалізація пунктуації
    text = re.sub(r'\s+([,.!?;:])', r'\1', text)  # Видаляємо пробіли перед пунктуацією
    text = re.sub(r'([,.!?;:])([^\s])', r'\1 \2', text)  # Додаємо пробіл після пунктуації

    # Специфічна обробка для різних мов
    if language == 'en':
        # Специфічна для англійської передобробка
        text = re.sub(r"[^a-zA-Z0-9,.!?;:'()\s-]", "", text)
        # Обробка скорочень
        text = re.sub(r"(\w)'(\w)", r"\1'\2", text)  # Збереження апострофів у скороченнях
    elif language == 'uk':
        # Специфічна для української передобробка
        text = re.sub(r"[^\u0400-\u04FF0-9,.!?;:'()\s-]", "", text)  # Збереження кириличних символів

    # Нормалізація пробілів
    text = re.sub(r'\s+', ' ', text).strip()

    return text

# Функція обчислення BLEU score
def calculate_bleu(reference, hypothesis):
    """
    Обчислює BLEU score для одного речення з використанням згладжування.
    """
    # Обчислюємо n-грами для n=1,2,3,4
    max_n = 4

    # Перевірка на порожні послідовності
    if len(reference) == 0 or len(hypothesis) == 0:
        return 0.0

    # Обчислюємо точність для кожного n
    precisions = []
    for n in range(1, min(max_n+1, len(hypothesis)+1)):
        # Створюємо n-грами для гіпотези
        hyp_ngrams = [tuple(hypothesis[i:i+n]) for i in range(len(hypothesis)-n+1)]

        # Створюємо n-грами для зразка
        ref_ngrams = [tuple(reference[i:i+n]) for i in range(len(reference)-n+1)]

        # Рахуємо збіги з обмеженням max-count
        hyp_counts = Counter(hyp_ngrams)
        ref_counts = Counter(ref_ngrams)

        # Рахуємо збіги з урахуванням кількості появ
        matches = sum(min(hyp_counts[ngram], ref_counts[ngram]) for ngram in hyp_counts)

        # Згладжування для уникнення нульових значень
        smooth = 1.0

        if len(hyp_ngrams) > 0:
            precisions.append((matches + smooth) / (len(hyp_ngrams) + smooth))
        else:
            precisions.append(0)

    # Обчислюємо середнє геометричне точностей
    if len(precisions) > 0:
        precision = np.exp(np.mean([np.log(p) for p in precisions]))
    else:
        precision = 0

    # Обчислюємо штраф за коротку довжину
    brevity_penalty = 1.0
    if len(hypothesis) < len(reference):
        brevity_penalty = np.exp(1 - len(reference) / max(len(hypothesis), 1))

    return brevity_penalty * precision

# Покращена функція обчислення BLEU
def calculate_enhanced_bleu(reference, hypothesis):
    """
    Покращений розрахунок BLEU score для одного речення з додатковими перевірками.
    """
    # Обчислюємо n-грами для n=1,2,3,4
    max_n = 4

    # Перевірка на порожні послідовності або надто короткі
    if len(reference) == 0 or len(hypothesis) == 0:
        return 0.0

    # Для надто коротких гіпотез зменшуємо максимальний порядок n-грамів
    if len(hypothesis) < max_n:
        max_n = len(hypothesis)

    # Обчислюємо точність для кожного n
    precisions = []
    for n in range(1, max_n+1):
        # Створюємо n-грами для гіпотези
        hyp_ngrams = [tuple(hypothesis[i:i+n]) for i in range(len(hypothesis)-n+1)]

        # Створюємо n-грами для зразка
        ref_ngrams = [tuple(reference[i:i+n]) for i in range(len(reference)-n+1)]

        # Рахуємо збіги з обмеженням max-count
        hyp_counts = Counter(hyp_ngrams)
        ref_counts = Counter(ref_ngrams)

        # Рахуємо збіги з урахуванням кількості появ
        matches = sum(min(hyp_counts[ngram], ref_counts[ngram]) for ngram in hyp_counts)

        # Удосконалене згладжування для уникнення нульових значень
        smooth = 1.0 / (2 ** (max_n - n))  # Збільшуємо згладжування для вищих порядків n-грам

        if len(hyp_ngrams) > 0:
            precisions.append((matches + smooth) / (len(hyp_ngrams) + smooth))
        else:
            precisions.append(0)

    # Обчислюємо середнє геометричне точностей
    if len(precisions) > 0:
        precision = np.exp(np.mean([np.log(max(p, 1e-10)) for p in precisions]))  # Запобігаємо log(0)
    else:
        precision = 0

    # Удосконалений штраф за коротку довжину
    brevity_penalty = np.exp(min(0, 1 - len(reference) / max(len(hypothesis), 1)))

    return brevity_penalty * precision

