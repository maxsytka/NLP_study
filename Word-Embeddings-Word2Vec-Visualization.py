# Клас енкодера LSTM з новими параметрами
class Encoder(nn.Module):
    def __init__(self, input_dim, emb_dim, hid_dim, n_layers, dropout):
        super(Encoder, self).__init__()
        self.hid_dim = hid_dim
        self.n_layers = n_layers

        # Шар для вбудовування слів у вектори
        self.embedding = nn.Embedding(input_dim, emb_dim)

        # Двонаправлений LSTM
        self.lstm = nn.LSTM(
            emb_dim,
            hid_dim,
            n_layers,
            dropout=dropout if n_layers > 1 else 0,
            batch_first=True,
            bidirectional=True
        )

        # Проекційні шари для прихованого стану
        self.fc_hidden = nn.Linear(hid_dim * 2, hid_dim)
        self.fc_cell = nn.Linear(hid_dim * 2, hid_dim)

        # Шар нормалізації
        self.layer_norm = nn.LayerNorm(hid_dim * 2)

        # Dropout для регуляризації
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        # x форма: (batch_size, sequence_length)

        # Проходимо через шар вбудовування
        embedded = self.dropout(self.embedding(x))
        # embedded форма: (batch_size, sequence_length, emb_dim)

        # Початкові приховані стани
        h_0 = torch.zeros(
            self.n_layers * 2,  # *2 для двонаправленого LSTM
            x.size(0),
            self.hid_dim
        ).to(x.device)

        c_0 = torch.zeros(
            self.n_layers * 2,
            x.size(0),
            self.hid_dim
        ).to(x.device)

        # Проходимо через LSTM
        outputs, (hidden, cell) = self.lstm(embedded, (h_0, c_0))

        # Застосовуємо нормалізацію
        outputs = self.layer_norm(outputs)

        # Об'єднуємо прямі та зворотні приховані стани
        hidden_forward = hidden[0:self.n_layers, :, :]
        hidden_backward = hidden[self.n_layers:, :, :]

        cell_forward = cell[0:self.n_layers, :, :]
        cell_backward = cell[self.n_layers:, :, :]

        hidden = torch.cat((hidden_forward[-1], hidden_backward[-1]), dim=1).unsqueeze(0).repeat(self.n_layers, 1, 1)
        cell = torch.cat((cell_forward[-1], cell_backward[-1]), dim=1).unsqueeze(0).repeat(self.n_layers, 1, 1)

        # Проекція до правильного розміру
        hidden = self.fc_hidden(hidden)
        cell = self.fc_cell(cell)

        return outputs, hidden, cell

# Клас декодера LSTM без механізму уваги з новими параметрами
class Decoder(nn.Module):
    def __init__(self, output_dim, emb_dim, hid_dim, n_layers, dropout):
        super(Decoder, self).__init__()
        self.output_dim = output_dim
        self.hid_dim = hid_dim
        self.n_layers = n_layers

        # Шар вбудовування
        self.embedding = nn.Embedding(output_dim, emb_dim)

        # LSTM без механізму уваги
        self.lstm = nn.LSTM(
            emb_dim,
            hid_dim,
            n_layers,
            dropout=dropout if n_layers > 1 else 0,
            batch_first=True
        )

        # Вихідний шар
        self.fc_out = nn.Linear(hid_dim, output_dim)

        # Dropout для регуляризації
        self.dropout = nn.Dropout(dropout)

    def forward(self, x, hidden, cell):
        # x форма: (batch_size)
        x = x.unsqueeze(1)  # Додаємо вимір для послідовності

        # Проходимо через шар вбудовування
        embedded = self.dropout(self.embedding(x))  # (batch_size, 1, emb_dim)

        # Проходимо через LSTM
        lstm_output, (hidden, cell) = self.lstm(embedded, (hidden, cell))  # (batch_size, 1, hid_dim)

        # Обчислюємо вихід
        output = self.fc_out(lstm_output.squeeze(1))  # (batch_size, output_dim)

        return output, hidden, cell

# Seq2Seq модель з новими параметрами
class Seq2Seq(nn.Module):
    def __init__(self, encoder, decoder, device):
        super(Seq2Seq, self).__init__()
        self.encoder = encoder
        self.decoder = decoder
        self.device = device
        self.teacher_forcing_ratio = TEACHER_FORCING_RATIO

    def forward(self, source, target, teacher_forcing_ratio=None):
        # Якщо коефіцієнт teacher forcing не надано, використовуємо стандартний
        if teacher_forcing_ratio is not None:
            self.teacher_forcing_ratio = teacher_forcing_ratio

        batch_size = source.shape[0]
        target_len = target.shape[1]

        # Ініціалізуємо тензор для зберігання виходів декодера
        outputs = torch.zeros(batch_size, target_len, self.decoder.output_dim).to(self.device)

        # Кодуємо вхідну послідовність
        encoder_outputs, hidden, cell = self.encoder(source)

        # Перший вхід до декодера - це токен SOS
        decoder_input = target[:, 0]

        for t in range(1, target_len):
            # Отримуємо вихід з декодера
            output, hidden, cell = self.decoder(decoder_input, hidden, cell)

            # Зберігаємо вихід
            outputs[:, t, :] = output

            # Teacher forcing: вирішуємо, чи використовувати передбачення або правильне слово
            teacher_force = random.random() < self.teacher_forcing_ratio

            # Отримуємо найкраще передбачення
            top1 = output.argmax(1)

            # Якщо teacher forcing, використовуємо правильне слово як наступний вхід
            # Інакше використовуємо найкраще передбачення
            decoder_input = target[:, t] if teacher_force else top1

        return outputs

# Функція перекладу з LSTM-моделлю без уваги
def lstm_translate_sentence(model, sentence, source_vocab, target_vocab, device, max_length=50):
    model.eval()

    # Передобробка речення
    sentence = preprocess_text(sentence, 'uk')

    # Перетворюємо в тензор
    try:
        tokens = [source_vocab.stoi[SOS_TOKEN]] + source_vocab.numericalize(sentence) + [source_vocab.stoi[EOS_TOKEN]]
        source_tensor = torch.LongTensor(tokens).unsqueeze(0).to(device)

        # Кодуємо речення
        with torch.no_grad():
            encoder_outputs, hidden, cell = model.encoder(source_tensor)

        # Ініціалізуємо вхід декодера
        decoder_input = torch.tensor([target_vocab.stoi[SOS_TOKEN]]).to(device)

        # Зберігаємо переклади
        translations = []

        for _ in range(max_length):
            with torch.no_grad():
                output, hidden, cell = model.decoder(decoder_input, hidden, cell)

            # Отримуємо передбачений токен
            predicted_token = output.argmax(1).item()

            # Зупиняємось, якщо це EOS
            if predicted_token == target_vocab.stoi[EOS_TOKEN]:
                break

            # Додаємо передбачений токен до перекладів
            if predicted_token in target_vocab.itos:
                translations.append(target_vocab.itos[predicted_token])

            # Оновлюємо вхід декодера
            decoder_input = torch.tensor([predicted_token]).to(device)

        return " ".join(translations)

    except KeyError as e:
        print(f"Помилка при перекладі: {e}")
        print(f"Доступні спеціальні токени: {[source_vocab.itos[i] for i in range(4)]}")
        print(f"Шуканий токен: {SOS_TOKEN}")

        return "Помилка перекладу - перевірте спеціальні токени"

# Функція для навчання моделі
def train_lstm_epoch(model, train_loader, optimizer, criterion, device, clip, teacher_forcing_ratio=None):
    model.train()
    epoch_loss = 0

    for batch in tqdm(train_loader, desc="Навчання"):
        source = batch["source"].to(device)
        target = batch["target"].to(device)

        # Обнуляємо градієнти
        optimizer.zero_grad()

        # Прямий прохід
        output = model(source, target, teacher_forcing_ratio)

        # Змінюємо форму виходу та цілі для функції втрат
        output_dim = output.shape[-1]
        output = output[:, 1:].reshape(-1, output_dim)  # Пропускаємо токен SOS
        target = target[:, 1:].reshape(-1)  # Пропускаємо токен SOS

        # Обчислюємо втрати
        loss = criterion(output, target)

        # Зворотнє розповсюдження
        loss.backward()

        # Обмеження градієнтів
        nn.utils.clip_grad_norm_(model.parameters(), clip)

        # Оновлюємо ваги
        optimizer.step()

        epoch_loss += loss.item()

    return epoch_loss / len(train_loader)

# Функція для оцінки моделі
def evaluate_lstm(model, data_loader, criterion, device):
    model.eval()
    epoch_loss = 0
    batch_bleu = []

    with torch.no_grad():
        for batch in tqdm(data_loader, desc="Оцінка"):
            source = batch["source"].to(device)
            target = batch["target"].to(device)

            # Прямий прохід без teacher forcing
            output = model(source, target, teacher_forcing_ratio=0)

            # Змінюємо форму виходу та цілі для функції втрат
            output_dim = output.shape[-1]
            output = output[:, 1:].reshape(-1, output_dim)  # Пропускаємо токен SOS
            target = target[:, 1:].reshape(-1)  # Пропускаємо токен SOS

            # Обчислюємо втрати
            loss = criterion(output, target)

            epoch_loss += loss.item()

            # Обчислюємо BLEU score
            output = output.reshape(batch["target"].shape[0], -1, output_dim)
            output = output.argmax(dim=2)

            for i in range(output.shape[0]):
                trg_len = batch["target"][i].ne(0).sum()
                trg = batch["target"][i, 1:trg_len-1].cpu().numpy()  # Видаляємо SOS та EOS

                pred_len = output[i].ne(0).sum()
                pred = output[i, :pred_len].cpu().numpy()

                # Конвертуємо індекси у слова
                trg_words = [english_vocab.itos[idx] for idx in trg if idx in english_vocab.itos]
                pred_words = [english_vocab.itos[idx] for idx in pred if idx in english_vocab.itos]

                # Обчислюємо BLEU для речення
                bleu = calculate_bleu(trg_words, pred_words)
                batch_bleu.append(bleu)

    avg_bleu = sum(batch_bleu) / len(batch_bleu) if batch_bleu else 0
    return epoch_loss / len(data_loader), avg_bleu

