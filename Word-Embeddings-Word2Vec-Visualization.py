# Покращена функція обчислення BLEU score
def calculate_bleu(reference, hypothesis):
    """
    Обчислює BLEU score для одного речення з використанням згладжування.
    """
    # Обчислюємо n-грами для n=1,2,3,4
    max_n = 4

    # Перевірка на порожні послідовності
    if len(reference) == 0 or len(hypothesis) == 0:
        return 0.0

    # Обчислюємо точність для кожного n
    precisions = []
    for n in range(1, min(max_n+1, len(hypothesis)+1)):
        # Створюємо n-грами для гіпотези
        hyp_ngrams = [tuple(hypothesis[i:i+n]) for i in range(len(hypothesis)-n+1)]

        # Створюємо n-грами для зразка
        ref_ngrams = [tuple(reference[i:i+n]) for i in range(len(reference)-n+1)]

        # Рахуємо збіги з обмеженням max-count
        hyp_counts = Counter(hyp_ngrams)
        ref_counts = Counter(ref_ngrams)

        # Рахуємо збіги з урахуванням кількості появ
        matches = sum(min(hyp_counts[ngram], ref_counts[ngram]) for ngram in hyp_counts)

        # Згладжування для уникнення нульових значень
        smooth = 1.0

        if len(hyp_ngrams) > 0:
            precisions.append((matches + smooth) / (len(hyp_ngrams) + smooth))
        else:
            precisions.append(0)

    # Обчислюємо середнє геометричне точностей
    if len(precisions) > 0:
        precision = np.exp(np.mean([np.log(p) for p in precisions]))
    else:
        precision = 0

    # Обчислюємо штраф за коротку довжину
    brevity_penalty = 1.0
    if len(hypothesis) < len(reference):
        brevity_penalty = np.exp(1 - len(reference) / max(len(hypothesis), 1))

    return brevity_penalty * precision

# Покращена функція для навчання LSTM-моделі на одну епоху
def train_lstm_epoch(model, train_loader, optimizer, criterion, device, clip, teacher_forcing_ratio=0.5):
    model.train()
    epoch_loss = 0

    for batch in tqdm(train_loader, desc="Навчання"):
        source = batch["source"].to(device)
        target = batch["target"].to(device)

        # Обнуляємо градієнти
        optimizer.zero_grad()

        # Прямий прохід
        output = model(source, target, teacher_forcing_ratio)

        # Змінюємо форму виходу та цілі для функції втрат
        output_dim = output.shape[-1]
        output = output[:, 1:].reshape(-1, output_dim)  # Пропускаємо токен SOS
        target = target[:, 1:].reshape(-1)  # Пропускаємо токен SOS

        # Обчислюємо втрати
        loss = criterion(output, target)

        # Зворотнє розповсюдження
        loss.backward()

        # Обмеження градієнтів
        nn.utils.clip_grad_norm_(model.parameters(), clip)

        # Оновлюємо ваги
        optimizer.step()

        epoch_loss += loss.item()

    return epoch_loss / len(train_loader)

# Покращена функція для оцінки LSTM-моделі
def evaluate_lstm(model, data_loader, criterion, device):
    model.eval()
    epoch_loss = 0
    batch_bleu = []

    with torch.no_grad():
        for batch in tqdm(data_loader, desc="Оцінка"):
            source = batch["source"].to(device)
            target = batch["target"].to(device)

            # Прямий прохід без teacher forcing
            output = model(source, target, teacher_forcing_ratio=0)

            # Змінюємо форму виходу та цілі для функції втрат
            output_dim = output.shape[-1]
            output = output[:, 1:].reshape(-1, output_dim)  # Пропускаємо токен SOS
            target = target[:, 1:].reshape(-1)  # Пропускаємо токен SOS

            # Обчислюємо втрати
            loss = criterion(output, target)

            epoch_loss += loss.item()

            # Обчислюємо BLEU score
            output = output.reshape(batch["target"].shape[0], -1, output_dim)
            output = output.argmax(dim=2)

            for i in range(output.shape[0]):
                trg_len = batch["target"][i].ne(0).sum()
                trg = batch["target"][i, 1:trg_len-1].cpu().numpy()  # Видаляємо SOS та EOS

                pred_len = output[i].ne(0).sum()
                pred = output[i, :pred_len].cpu().numpy()

                # Конвертуємо індекси у слова
                trg_words = [english_vocab.itos[idx] for idx in trg if idx in english_vocab.itos]
                pred_words = [english_vocab.itos[idx] for idx in pred if idx in english_vocab.itos]

                # Обчислюємо BLEU для речення
                bleu = calculate_bleu(trg_words, pred_words)
                batch_bleu.append(bleu)

    avg_bleu = sum(batch_bleu) / len(batch_bleu) if batch_bleu else 0
    return epoch_loss / len(data_loader), avg_bleu

