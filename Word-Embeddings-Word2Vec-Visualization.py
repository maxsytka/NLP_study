# Ансамблева модель для покращення якості перекладу
def ensemble_translate(sentence, lstm_model, transformer_model, src_vocab, tgt_vocab, device, max_len=50):
    """
    Виконує переклад з використанням обох моделей (LSTM і Transformer) та об'єднує їх результати
    для отримання більш точного перекладу.
    """
    # Отримуємо переклад від LSTM-моделі
    lstm_translation = lstm_translate_sentence(lstm_model, sentence, src_vocab, tgt_vocab, device, max_len)
    lstm_tokens = simple_tokenize(lstm_translation)

    # Отримуємо переклад від Transformer-моделі
    transformer_translation = transformer_translate_sentence(transformer_model, sentence, src_vocab, tgt_vocab, device, max_len)
    transformer_tokens = simple_tokenize(transformer_translation)

    # Якщо одна з моделей дає набагато коротший або порожній результат, використовуємо іншу
    if len(lstm_tokens) < 2 and len(transformer_tokens) > 2:
        return transformer_translation
    elif len(transformer_tokens) < 2 and len(lstm_tokens) > 2:
        return lstm_translation
    elif len(lstm_tokens) < 2 and len(transformer_tokens) < 2:
        # Якщо обидві моделі дали порожній результат, повертаємо заглушку
        return "Не вдалося виконати переклад."

    # Визначаємо ваги для кожної моделі (трансформеру даємо більшу вагу)
    lstm_weight = 0.35
    transformer_weight = 0.65

    # Беремо переклад з вищою вагою як основу і покращуємо його
    if transformer_weight > lstm_weight:
        base_translation = transformer_translation
        # Додаємо унікальні інформативні слова з LSTM-перекладу, які відсутні в Transformer-перекладі
        additional_words = [word for word in lstm_tokens if word not in transformer_tokens
                           and word not in ['a', 'the', 'is', 'are', 'in', 'on', 'at', 'and', 'or']]

        if additional_words:
            # Намагаємося вставити додаткові слова у відповідний контекст
            result_tokens = transformer_tokens.copy()
            for word in additional_words:
                # Перевіряємо, чи слово відсутнє в результаті
                if word not in result_tokens:
                    # Знаходимо найкращу позицію для вставки
                    # Для простоти, додаємо в кінець
                    result_tokens.append(word)

            return " ".join(result_tokens)
        else:
            return base_translation
    else:
        # Аналогічно для LSTM як основи
        base_translation = lstm_translation
        additional_words = [word for word in transformer_tokens if word not in lstm_tokens
                           and word not in ['a', 'the', 'is', 'are', 'in', 'on', 'at', 'and', 'or']]

        if additional_words:
            result_tokens = lstm_tokens.copy()
            for word in additional_words:
                if word not in result_tokens:
                    result_tokens.append(word)

            return " ".join(result_tokens)
        else:
            return base_translation

