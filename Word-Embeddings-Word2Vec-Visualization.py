# Згладжування міток для кращої генералізації
class LabelSmoothingLoss(nn.Module):
    def __init__(self, smoothing=0.1, ignore_index=0):
        super(LabelSmoothingLoss, self).__init__()
        self.smoothing = smoothing
        self.ignore_index = ignore_index
        self.criterion = nn.KLDivLoss(reduction='sum')

    def forward(self, output, target):
        # Створюємо маску для ігнорування padding
        mask = (target != self.ignore_index).float()
        mask_sum = mask.sum()

        # Якщо всі токени - padding, повертаємо 0
        if mask_sum == 0:
            return torch.tensor(0.0, device=output.device)

        # Кількість класів
        n_class = output.size(1)

        # Перетворюємо цілі у one-hot вектори
        one_hot = torch.zeros_like(output).scatter(1, target.unsqueeze(1), 1)

        # Додаємо згладжування
        one_hot = one_hot * (1.0 - self.smoothing) + self.smoothing / n_class

        # Застосовуємо маску для ігнорування padding
        one_hot = one_hot * mask.unsqueeze(1)

        # Застосовуємо log_softmax до виходу
        log_prob = F.log_softmax(output, dim=1)

        # Обчислюємо втрати
        loss = self.criterion(log_prob, one_hot)

        # Нормалізуємо за кількістю токенів (без padding)
        loss = loss / mask_sum

        return loss

# Визначаємо оптимізатор і функцію втрат для трансформера
transformer_criterion = LabelSmoothingLoss(smoothing=0.1, ignore_index=0)
transformer_optimizer = optim.Adam(transformer_model.parameters(), lr=TRANSFORMER_LR, betas=(0.9, 0.98), eps=1e-9)

# Планувальник швидкості навчання для трансформера
transformer_scheduler = optim.lr_scheduler.OneCycleLR(transformer_optimizer,
                                              max_lr=TRANSFORMER_LR,
                                              steps_per_epoch=len(train_loader),
                                              epochs=TRANSFORMER_EPOCHS)

# Функція для навчання трансформера
def train_transformer_epoch(model, data_loader, optimizer, criterion, device):
    model.train()
    epoch_loss = 0

    for batch in tqdm(data_loader, desc="Навчання трансформера"):
        src = batch["source"].to(device)
        tgt = batch["target"].to(device)

        # Створюємо маски для src і tgt
        src_mask = model.make_src_mask(src)
        tgt_mask = model.make_tgt_mask(tgt[:, :-1])  # Виключаємо останній токен (EOS)

        # Обнуляємо градієнти
        optimizer.zero_grad()

        # Прямий прохід
        output = model(src, tgt[:, :-1], src_mask, tgt_mask)

        # Змінюємо форму виходу та цілі для функції втрат
        output_dim = output.shape[-1]
        output = output.contiguous().view(-1, output_dim)
        tgt = tgt[:, 1:].contiguous().view(-1)  # Виключаємо перший токен (SOS)

        # Обчислюємо втрати
        loss = criterion(output, tgt)

        # Зворотнє розповсюдження
        loss.backward()

        # Обмеження градієнтів
        nn.utils.clip_grad_norm_(model.parameters(), CLIP)

        # Оновлюємо ваги
        optimizer.step()

        # Оновлюємо планувальник
        transformer_scheduler.step()

        epoch_loss += loss.item()

    return epoch_loss / len(data_loader)

# Функція для оцінки трансформера
def evaluate_transformer(model, data_loader, criterion, device):
    model.eval()
    epoch_loss = 0
    batch_bleu = []

    with torch.no_grad():
        for batch in tqdm(data_loader, desc="Оцінка трансформера"):
            src = batch["source"].to(device)
            tgt = batch["target"].to(device)

            # Створюємо маски для src і tgt
            src_mask = model.make_src_mask(src)
            tgt_mask = model.make_tgt_mask(tgt[:, :-1])

            # Прямий прохід
            output = model(src, tgt[:, :-1], src_mask, tgt_mask)

            # Змінюємо форму виходу та цілі для функції втрат
            output_dim = output.shape[-1]
            output = output.contiguous().view(-1, output_dim)
            tgt = tgt[:, 1:].contiguous().view(-1)

            # Обчислюємо втрати
            loss = criterion(output, tgt)

            epoch_loss += loss.item()

            # Обчислюємо BLEU score
            output = output.view(batch["target"].size(0), -1, output_dim)
            output = output.argmax(dim=2)

            for i in range(output.shape[0]):
                trg_len = batch["target"][i].ne(0).sum()
                trg = batch["target"][i, 1:trg_len-1].cpu().numpy()  # Видаляємо SOS та EOS

                pred = output[i].cpu().numpy()

                # Конвертуємо індекси у слова
                trg_words = [english_vocab.itos[idx] for idx in trg if idx in english_vocab.itos]
                pred_words = [english_vocab.itos[idx] for idx in pred if idx in english_vocab.itos]

                # Обчислюємо BLEU для речення
                bleu = calculate_bleu(trg_words, pred_words)
                batch_bleu.append(bleu)

    avg_bleu = sum(batch_bleu) / len(batch_bleu) if batch_bleu else 0
    return epoch_loss / len(data_loader), avg_bleu

# Покращена функція для перекладу речення за допомогою трансформера
def transformer_translate_sentence(model, sentence, src_vocab, tgt_vocab, device, max_len=50):
    model.eval()

    # Передобробка речення
    sentence = preprocess_text(sentence, 'uk')

    try:
        # Переконаймося, що використовуємо правильні спеціальні токени
        tokens = [src_vocab.stoi[SOS_TOKEN]] + src_vocab.numericalize(sentence) + [src_vocab.stoi[EOS_TOKEN]]
        src_tensor = torch.LongTensor(tokens).unsqueeze(0).to(device)

        # Створюємо маску для src
        src_mask = model.make_src_mask(src_tensor)

        # Кодуємо вхідну послідовність
        enc_output = model.encoder(src_tensor, src_mask)

        # Створюємо тензор для збереження виходу
        outputs = [tgt_vocab.stoi[SOS_TOKEN]]

        # Декодуємо послідовно
        for i in range(max_len):
            tgt_tensor = torch.LongTensor(outputs).unsqueeze(0).to(device)
            tgt_mask = model.make_tgt_mask(tgt_tensor)

            with torch.no_grad():
                output = model.decoder(tgt_tensor, enc_output, src_mask, tgt_mask)

            # Отримуємо найімовірніший наступний токен
            pred_token = output[:, -1, :].argmax(dim=1).item()

            # Додаємо передбачений токен до виходу
            outputs.append(pred_token)

            # Зупиняємось, якщо це EOS
            if pred_token == tgt_vocab.stoi[EOS_TOKEN]:
                break

        # Перетворюємо токени у слова, пропускаючи спеціальні токени
        translated_tokens = [tgt_vocab.itos[token] for token in outputs[1:-1]]  # Пропускаємо SOS і EOS
        return ' '.join(translated_tokens)

    except KeyError as e:
        print(f"Помилка при перекладі: {e}")
        print(f"Доступні спеціальні токени: {[src_vocab.itos[i] for i in range(4)]}")
        print(f"Шуканий токен: {SOS_TOKEN}")

        # Запасний варіант - використовуємо індекси спеціальних токенів
        tokens = [1] + src_vocab.numericalize(sentence) + [2]  # 1=SOS, 2=EOS
        src_tensor = torch.LongTensor(tokens).unsqueeze(0).to(device)

        # Виконуємо переклад з фіксованими індексами
        src_mask = model.make_src_mask(src_tensor)
        enc_output = model.encoder(src_tensor, src_mask)

        outputs = [1]  # Початковий SOS токен

        for i in range(max_len):
            tgt_tensor = torch.LongTensor(outputs).unsqueeze(0).to(device)
            tgt_mask = model.make_tgt_mask(tgt_tensor)
            tgt_tensor = torch.LongTensor(outputs).unsqueeze(0).to(device)
            tgt_mask = model.make_tgt_mask(tgt_tensor)

            with torch.no_grad():
                output = model.decoder(tgt_tensor, enc_output, src_mask, tgt_mask)

            pred_token = output[:, -1, :].argmax(dim=1).item()
            outputs.append(pred_token)

            if pred_token == 2:  # EOS токен
                break

        # Перетворюємо токени в текст
        translated_tokens = [tgt_vocab.itos[token] for token in outputs[1:-1] if token in tgt_vocab.itos]
        return ' '.join(translated_tokens)

