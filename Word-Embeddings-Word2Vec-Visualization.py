        source_tensor += self.source_vocab.numericalize(source_text)
        source_tensor.append(self.source_vocab.stoi[EOS_TOKEN])

        # Перетворюємо цільовий текст у числову форму
        target_tensor = [self.target_vocab.stoi[SOS_TOKEN]]
        target_tensor += self.target_vocab.numericalize(target_text)
        target_tensor.append(self.target_vocab.stoi[EOS_TOKEN])

        return {
            "source_text": source_text,
            "target_text": target_text,
            "source": torch.tensor(source_tensor),
            "target": torch.tensor(target_tensor)
        }

