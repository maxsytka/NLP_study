# Клас енкодера LSTM (покращений)
class Encoder(nn.Module):
    def __init__(self, input_size, embedding_size, hidden_size, num_layers, dropout):
        super(Encoder, self).__init__()
        self.hidden_size = hidden_size
        self.num_layers = num_layers

        # Шар для вбудовування слів у вектори
        self.embedding = nn.Embedding(input_size, embedding_size)

        # Двонаправлений LSTM
        self.lstm = nn.LSTM(
            embedding_size,
            hidden_size,
            num_layers,
            dropout=dropout if num_layers > 1 else 0,
            batch_first=True,
            bidirectional=True
        )

        # Повнозв'язні шари для підготовки прихованого стану для декодера
        self.fc_hidden = nn.Linear(hidden_size * 2, hidden_size)
        self.fc_cell = nn.Linear(hidden_size * 2, hidden_size)

        # Застосовуємо нормалізацію для стабільнішого навчання
        self.layer_norm = nn.LayerNorm(hidden_size * 2)

        # Dropout для регуляризації
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):
        # x форма: (batch_size, sequence_length)

        embedding = self.dropout(self.embedding(x))
        # embedding форма: (batch_size, sequence_length, embedding_size)

        # Початкові приховані стани
        h_0 = torch.zeros(
            self.num_layers * 2,  # *2 для двонаправленого LSTM
            x.size(0),
            self.hidden_size
        ).to(x.device)

        c_0 = torch.zeros(
            self.num_layers * 2,
            x.size(0),
            self.hidden_size
        ).to(x.device)

        # Проходимо через LSTM
        outputs, (hidden, cell) = self.lstm(embedding, (h_0, c_0))

        # Застосовуємо нормалізацію шару
        outputs = self.layer_norm(outputs)

        # Об'єднуємо прямі та зворотні приховані стани
        hidden_forward = hidden[0:self.num_layers, :, :]
        hidden_backward = hidden[self.num_layers:, :, :]

        cell_forward = cell[0:self.num_layers, :, :]
        cell_backward = cell[self.num_layers:, :, :]

        hidden = torch.cat((hidden_forward[-1], hidden_backward[-1]), dim=1).unsqueeze(0).repeat(self.num_layers, 1, 1)
        cell = torch.cat((cell_forward[-1], cell_backward[-1]), dim=1).unsqueeze(0).repeat(self.num_layers, 1, 1)

        # Проекція до правильного розміру для декодера
        hidden = self.fc_hidden(hidden)
        cell = self.fc_cell(cell)

        return outputs, hidden, cell

# Клас декодера LSTM з механізмом уваги
class AttentionDecoder(nn.Module):
    def __init__(self, input_size, embedding_size, hidden_size, output_size, num_layers, dropout):
        super(AttentionDecoder, self).__init__()
        self.hidden_size = hidden_size
        self.num_layers = num_layers
        self.output_size = output_size

        # Шар вбудовування
        self.embedding = nn.Embedding(input_size, embedding_size)

        # Механізм уваги
        self.attention = nn.Linear(hidden_size * 2 + hidden_size, hidden_size)
        self.energy = nn.Linear(hidden_size, 1, bias=False)

        # LSTM з розширеним вхідним розміром для включення контексту
        self.lstm = nn.LSTM(
            embedding_size + hidden_size * 2,
            hidden_size,
            num_layers,
            dropout=dropout if num_layers > 1 else 0,
            batch_first=True
        )

        # Вихідний шар
        self.fc_out = nn.Linear(hidden_size * 2 + hidden_size + embedding_size, output_size)

        # Dropout для регуляризації
        self.dropout = nn.Dropout(dropout)

    def forward(self, x, hidden, cell, encoder_outputs):
        # x форма: (batch_size)
        x = x.unsqueeze(1)  # додаємо вимір для послідовності

        # Проходимо через шар вбудовування
        embedded = self.dropout(self.embedding(x))  # (batch_size, 1, embedding_size)

        # Підготовка для обчислення уваги
        batch_size = encoder_outputs.size(0)
        src_len = encoder_outputs.size(1)

        # Останній прихований стан декодера
        hidden_for_attention = hidden[-1].unsqueeze(1).repeat(1, src_len, 1)  # (batch_size, src_len, hidden_size)

        # Об'єднуємо енкодер та декодер для обчислення уваги
        attention_input = torch.cat(
            (hidden_for_attention, encoder_outputs), dim=2
        )  # (batch_size, src_len, hidden_size*3)

        # Обчислюємо енергію та ваги уваги
        energy = torch.tanh(self.attention(attention_input))  # (batch_size, src_len, hidden_size)
        attention_scores = self.energy(energy).squeeze(2)  # (batch_size, src_len)

        # Застосовуємо softmax для отримання ваг уваги
        attention_weights = F.softmax(attention_scores, dim=1).unsqueeze(1)  # (batch_size, 1, src_len)

        # Обчислюємо контекстний вектор
        context_vector = torch.bmm(attention_weights, encoder_outputs)  # (batch_size, 1, hidden_size*2)

        # Об'єднуємо вбудовування та контекстний вектор для LSTM
        lstm_input = torch.cat((embedded, context_vector), dim=2)  # (batch_size, 1, embedding_size + hidden_size*2)

        # Проходимо через LSTM
        lstm_output, (hidden, cell) = self.lstm(lstm_input, (hidden, cell))  # (batch_size, 1, hidden_size)

        # Підготовка до вихідного шару
        output = torch.cat(
            (lstm_output, context_vector, embedded), dim=2
        )  # (batch_size, 1, hidden_size + hidden_size*2 + embedding_size)

        # Обчислюємо вихід
        output = self.fc_out(output.squeeze(1))  # (batch_size, output_size)

        return output, hidden, cell, attention_weights

# Seq2Seq модель з увагою
class Seq2SeqWithAttention(nn.Module):
    def __init__(
        self, encoder, decoder, target_vocab_size, device, teacher_forcing_ratio=0.5
    ):
        super(Seq2SeqWithAttention, self).__init__()
        self.encoder = encoder
        self.decoder = decoder
        self.target_vocab_size = target_vocab_size
        self.device = device
        self.teacher_forcing_ratio = teacher_forcing_ratio

    def forward(self, source, target, teacher_forcing_ratio=None):
        # Якщо коефіцієнт teacher forcing не надано, використовуємо стандартний
        if teacher_forcing_ratio is not None:
            self.teacher_forcing_ratio = teacher_forcing_ratio

        batch_size = source.shape[0]
        target_len = target.shape[1]

        # Ініціалізуємо тензор для зберігання виходів декодера
        outputs = torch.zeros(batch_size, target_len, self.target_vocab_size).to(self.device)

        # Кодуємо вхідну послідовність
        encoder_outputs, hidden, cell = self.encoder(source)

        # Перший вхід до декодера - це токен SOS
        decoder_input = target[:, 0]

        for t in range(1, target_len):
            # Отримуємо вихід з декодера з увагою
            output, hidden, cell, attention_weights = self.decoder(
                decoder_input, hidden, cell, encoder_outputs
            )

            # Зберігаємо вихід
            outputs[:, t, :] = output

            # Teacher forcing: вирішуємо, чи використовувати передбачення або правильне слово
            teacher_force = random.random() < self.teacher_forcing_ratio

            # Отримуємо найкраще передбачення
            top1 = output.argmax(1)

            # Якщо teacher forcing, використовуємо правильне слово як наступний вхід
            # Інакше використовуємо найкраще передбачення
            decoder_input = target[:, t] if teacher_force else top1

        return outputs

