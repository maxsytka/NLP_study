# Клас для створення датасету для навчання
class TranslationDataset(Dataset):
    def __init__(self, df, source_vocab, target_vocab, source_lang, target_lang):
        self.df = df
        self.source_vocab = source_vocab
        self.target_vocab = target_vocab
        self.source_lang = source_lang
        self.target_lang = target_lang

    def __len__(self):
        return len(self.df)

    def __getitem__(self, index):
        source_text = self.df.iloc[index][f'{self.source_lang}_processed']
        target_text = self.df.iloc[index][f'{self.target_lang}_processed']

        # Перетворюємо вхідний текст у числову форму
        source_tensor = [self.source_vocab.stoi[SOS_TOKEN]]
        source_tensor += self.source_vocab.numericalize(source_text)
        source_tensor.append(self.source_vocab.stoi[EOS_TOKEN])

        # Перетворюємо цільовий текст у числову форму
        target_tensor = [self.target_vocab.stoi[SOS_TOKEN]]
        target_tensor += self.target_vocab.numericalize(target_text)
        target_tensor.append(self.target_vocab.stoi[EOS_TOKEN])

        return {
            "source_text": source_text,
            "target_text": target_text,
            "source": torch.tensor(source_tensor),
            "target": torch.tensor(target_tensor)
        }

# Покращена функція для батчингу
def collate_fn(batch):
    source_texts = [item["source_text"] for item in batch]
    target_texts = [item["target_text"] for item in batch]
    source_tensors = [item["source"] for item in batch]
    target_tensors = [item["target"] for item in batch]

    # Доповнюємо послідовності до однакової довжини
    source_tensors_padded = pad_sequence(source_tensors, batch_first=True, padding_value=0)
    target_tensors_padded = pad_sequence(target_tensors, batch_first=True, padding_value=0)

    # Обчислюємо маски для послідовностей (1 для реальних токенів, 0 для padding)
    source_mask = (source_tensors_padded != 0).float()
    target_mask = (target_tensors_padded != 0).float()

    return {
        "source_texts": source_texts,
        "target_texts": target_texts,
        "source": source_tensors_padded,
        "target": target_tensors_padded,
        "source_mask": source_mask,
        "target_mask": target_mask
    }

