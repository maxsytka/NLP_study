# Позиційне кодування
class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_len=5000):
        super(PositionalEncoding, self).__init__()
        self.dropout = nn.Dropout(p=0.1)

        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len, dtype=torch.float).unsqueeze(1)
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-np.log(10000.0) / d_model))

        pe[:, 0::2] = torch.sin(position * div_term)
        pe[:, 1::2] = torch.cos(position * div_term)
        pe = pe.unsqueeze(0).transpose(0, 1)

        self.register_buffer('pe', pe)

    def forward(self, x):
        x = x + self.pe[:x.size(0), :]
        return self.dropout(x)

# Клас уваги із багатьма головками
class MultiHeadAttention(nn.Module):
    def __init__(self, d_model, num_heads):
        super(MultiHeadAttention, self).__init__()
        assert d_model % num_heads == 0

        self.d_k = d_model // num_heads
        self.num_heads = num_heads

        self.query = nn.Linear(d_model, d_model)
        self.key = nn.Linear(d_model, d_model)
        self.value = nn.Linear(d_model, d_model)
        self.out = nn.Linear(d_model, d_model)

    def forward(self, q, k, v, mask=None):
        batch_size = q.size(0)

        # Лінійна проекція для отримання запиту, ключа та значення
        q = self.query(q).view(batch_size, -1, self.num_heads, self.d_k).transpose(1, 2)
        k = self.key(k).view(batch_size, -1, self.num_heads, self.d_k).transpose(1, 2)
        v = self.value(v).view(batch_size, -1, self.num_heads, self.d_k).transpose(1, 2)

        # Матричне множення для отримання уваги
        scores = torch.matmul(q, k.transpose(-2, -1)) / (self.d_k ** 0.5)

        # Застосовуємо маску, якщо вона є
        if mask is not None:
            scores = scores.masked_fill(mask == 0, -1e9)

        # Застосовуємо softmax і отримуємо значення
        attn = F.softmax(scores, dim=-1)
        output = torch.matmul(attn, v)

        # Об'єднуємо голови і застосовуємо останній лінійний шар
        output = output.transpose(1, 2).contiguous().view(batch_size, -1, self.num_heads * self.d_k)
        return self.out(output)

# Шар подачі вперед
class FeedForward(nn.Module):
    def __init__(self, d_model, d_ff, dropout=0.1):
        super(FeedForward, self).__init__()
        self.linear1 = nn.Linear(d_model, d_ff)
        self.dropout = nn.Dropout(dropout)
        self.linear2 = nn.Linear(d_ff, d_model)

    def forward(self, x):
        return self.linear2(self.dropout(F.relu(self.linear1(x))))

# Блок енкодера
class EncoderLayer(nn.Module):
    def __init__(self, d_model, num_heads, d_ff, dropout=0.1):
        super(EncoderLayer, self).__init__()
        self.self_attn = MultiHeadAttention(d_model, num_heads)
        self.feed_forward = FeedForward(d_model, d_ff, dropout)
        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.dropout = nn.Dropout(dropout)

    def forward(self, x, mask):
        # Застосовуємо само-увагу
        attn_output = self.self_attn(x, x, x, mask)
        x = self.norm1(x + self.dropout(attn_output))

        # Застосовуємо подачу вперед
        ff_output = self.feed_forward(x)
        x = self.norm2(x + self.dropout(ff_output))
        return x

# Блок декодера
class DecoderLayer(nn.Module):
    def __init__(self, d_model, num_heads, d_ff, dropout=0.1):
        super(DecoderLayer, self).__init__()
        self.self_attn = MultiHeadAttention(d_model, num_heads)
        self.cross_attn = MultiHeadAttention(d_model, num_heads)
        self.feed_forward = FeedForward(d_model, d_ff, dropout)

        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.norm3 = nn.LayerNorm(d_model)

        self.dropout = nn.Dropout(dropout)

    def forward(self, x, enc_output, src_mask, tgt_mask):
        # Застосовуємо маскованy само-увагу
        attn_output = self.self_attn(x, x, x, tgt_mask)
        x = self.norm1(x + self.dropout(attn_output))

        # Застосовуємо перехресну увагу
        attn_output = self.cross_attn(x, enc_output, enc_output, src_mask)
        x = self.norm2(x + self.dropout(attn_output))

        # Застосовуємо подачу вперед
        ff_output = self.feed_forward(x)
        x = self.norm3(x + self.dropout(ff_output))
        return x

# Клас енкодера
class TransformerEncoder(nn.Module):
    def __init__(self, vocab_size, d_model, num_heads, d_ff, num_layers, dropout=0.1):
        super(TransformerEncoder, self).__init__()
        self.embedding = nn.Embedding(vocab_size, d_model)
        self.pos_encoding = PositionalEncoding(d_model)
        self.layers = nn.ModuleList([EncoderLayer(d_model, num_heads, d_ff, dropout) for _ in range(num_layers)])
        self.dropout = nn.Dropout(dropout)
        self.d_model = d_model

    def forward(self, x, mask):
        # x форма: (batch_size, seq_len)
        seq_len = x.size(1)

        # Вбудовування та масштабування
        x = self.embedding(x) * (self.d_model ** 0.5)

        # Додавання позиційного кодування
        x = self.pos_encoding(x.transpose(0, 1)).transpose(0, 1)

        # Застосовуємо dropout
        x = self.dropout(x)

        # Проходимо через усі шари енкодера
        for layer in self.layers:
            x = layer(x, mask)

        return x

# Клас декодера
class TransformerDecoder(nn.Module):
    def __init__(self, vocab_size, d_model, num_heads, d_ff, num_layers, dropout=0.1):
        super(TransformerDecoder, self).__init__()
        self.embedding = nn.Embedding(vocab_size, d_model)
        self.pos_encoding = PositionalEncoding(d_model)
        self.layers = nn.ModuleList([DecoderLayer(d_model, num_heads, d_ff, dropout) for _ in range(num_layers)])
        self.fc_out = nn.Linear(d_model, vocab_size)
        self.dropout = nn.Dropout(dropout)
        self.d_model = d_model

    def forward(self, x, enc_output, src_mask, tgt_mask):
        # x форма: (batch_size, seq_len)
        seq_len = x.size(1)

        # Вбудовування та масштабування
        x = self.embedding(x) * (self.d_model ** 0.5)

        # Додавання позиційного кодування
        x = self.pos_encoding(x.transpose(0, 1)).transpose(0, 1)

        # Застосовуємо dropout
        x = self.dropout(x)

        # Проходимо через усі шари декодера
        for layer in self.layers:
            x = layer(x, enc_output, src_mask, tgt_mask)

        # Проекція на словник
        output = self.fc_out(x)

        return output

# Клас трансформера
class Transformer(nn.Module):
    def __init__(self, src_vocab_size, tgt_vocab_size, d_model, num_heads, d_ff, num_layers, dropout=0.1):
        super(Transformer, self).__init__()
        self.encoder = TransformerEncoder(src_vocab_size, d_model, num_heads, d_ff, num_layers, dropout)
        self.decoder = TransformerDecoder(tgt_vocab_size, d_model, num_heads, d_ff, num_layers, dropout)

    def forward(self, src, tgt, src_mask, tgt_mask):
        # Кодуємо вхідну послідовність
        enc_output = self.encoder(src, src_mask)

        # Декодуємо з використанням закодованої послідовності
        output = self.decoder(tgt, enc_output, src_mask, tgt_mask)

        return output

    def make_src_mask(self, src):
        # src форма: (batch_size, src_len)
        src_mask = (src != 0).unsqueeze(1).unsqueeze(2)
        # src_mask форма: (batch_size, 1, 1, src_len)
        return src_mask

    def make_tgt_mask(self, tgt):
        # tgt форма: (batch_size, tgt_len)
        batch_size, tgt_len = tgt.size()

        # Маска padding
        tgt_pad_mask = (tgt != 0).unsqueeze(1).unsqueeze(3)
        # tgt_pad_mask форма: (batch_size, 1, tgt_len, 1)

        # Маска для автоматичної регресії (наступні позиції)
        subsequent_mask = torch.tril(torch.ones((tgt_len, tgt_len), device=tgt.device)).bool()
        # subsequent_mask форма: (tgt_len, tgt_len)

        subsequent_mask = subsequent_mask.unsqueeze(0).unsqueeze(1)
        # subsequent_mask форма: (1, 1, tgt_len, tgt_len)

        tgt_mask = tgt_pad_mask & subsequent_mask
        # tgt_mask форма: (batch_size, 1, tgt_len, tgt_len)

        return tgt_mask

