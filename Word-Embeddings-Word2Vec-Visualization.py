# Обчислення BLEU score для тестової вибірки
def calculate_model_bleu(model_type, model, test_loader, src_vocab, tgt_vocab, device):
    """
    Обчислює BLEU score для моделі на тестовій вибірці
    """
    model.eval()
    bleu_scores = []

    with torch.no_grad():
        for batch in tqdm(test_loader, desc=f"Обчислення BLEU для {model_type}"):
            src = batch["source"].to(device)
            tgt = batch["target"]

            for i in range(src.size(0)):
                src_sentence = src[i, :].unsqueeze(0)
                target_sentence = tgt[i, :]

                # Отримуємо оригінальний текст
                original_indices = [idx.item() for idx in target_sentence if idx.item() not in [0, 1, 2]]
                original_words = [tgt_vocab.itos[idx] for idx in original_indices if idx in tgt_vocab.itos]

                # Отримуємо вхідний текст для перекладу
                input_sentence = " ".join([src_vocab.itos[idx.item()] for idx in src_sentence[0]
                                          if idx.item() > 3 and idx.item() in src_vocab.itos])

                # Виконуємо переклад
                if model_type == "LSTM":
                    translation = lstm_translate_sentence(model, input_sentence, src_vocab, tgt_vocab, device)
                else:  # Transformer
                    translation = transformer_translate_sentence(model, input_sentence, src_vocab, tgt_vocab, device)

                # Токенізуємо переклад
                translated_words = simple_tokenize(translation)

                # Обчислюємо BLEU score
                bleu = calculate_bleu(original_words, translated_words)
                bleu_scores.append(bleu)

                # Обмежуємо кількість прикладів для прискорення
                if len(bleu_scores) >= 100:
                    break

            if len(bleu_scores) >= 100:
                break

    return sum(bleu_scores) / len(bleu_scores) if bleu_scores else 0

