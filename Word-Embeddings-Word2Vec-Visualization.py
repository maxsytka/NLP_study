        tokens = [source_vocab.stoi[SOS_TOKEN]] + source_vocab.numericalize(sentence) + [source_vocab.stoi[EOS_TOKEN]]
        source_tensor = torch.LongTensor(tokens).unsqueeze(0).to(device)

        # Кодуємо речення
        with torch.no_grad():
            encoder_outputs, hidden, cell = model.encoder(source_tensor)

        # Ініціалізуємо вхід декодера
        decoder_input = torch.tensor([target_vocab.stoi[SOS_TOKEN]]).to(device)

        # Зберігаємо переклади
        translations = []

        for _ in range(max_length):
            with torch.no_grad():
                output, hidden, cell, attention_weights = model.decoder(
                    decoder_input, hidden, cell, encoder_outputs
                )

