# Клас для створення словників з покращеннями
class Vocabulary:
    def __init__(self, freq_threshold=2):
        # Спеціальні токени: padding, start of sentence, end of sentence, unknown
        self.itos = {0: PAD_TOKEN, 1: SOS_TOKEN, 2: EOS_TOKEN, 3: UNK_TOKEN}
        self.stoi = {PAD_TOKEN: 0, SOS_TOKEN: 1, EOS_TOKEN: 2, UNK_TOKEN: 3}
        self.freq_threshold = freq_threshold

    def __len__(self):
        return len(self.itos)

    def build_vocabulary(self, sentence_list):
        """Побудова словника з частотним порогом"""
        frequencies = Counter()
        idx = 4

        for sentence in sentence_list:
            for word in simple_tokenize(sentence):
                frequencies[word] += 1

                # Додаємо слово до словника, якщо його частота досягла порогу
                if frequencies[word] == self.freq_threshold:
                    self.stoi[word] = idx
                    self.itos[idx] = word
                    idx += 1

    def numericalize(self, text):
        """Перетворює текст у послідовність чисел (індекси слів у словнику)"""
        tokenized_text = simple_tokenize(text)

        return [
            self.stoi[token] if token in self.stoi else self.stoi[UNK_TOKEN]
            for token in tokenized_text
        ]

    def convert_to_text(self, indices):
        """Перетворює послідовність індексів назад у текст"""
        tokens = [self.itos[idx] for idx in indices if idx in self.itos]
        # Видаляємо спеціальні токени
        tokens = [token for token in tokens if token not in [PAD_TOKEN, SOS_TOKEN, EOS_TOKEN]]
        return " ".join(tokens)

