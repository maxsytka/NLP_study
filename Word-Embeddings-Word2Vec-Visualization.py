# Згладжування міток для кращої генералізації
class LabelSmoothingLoss(nn.Module):
    def __init__(self, smoothing=0.1, ignore_index=0):
        super(LabelSmoothingLoss, self).__init__()
        self.smoothing = smoothing
        self.ignore_index = ignore_index
        self.criterion = nn.KLDivLoss(reduction='sum')

    def forward(self, output, target):
        # Створюємо маску для ігнорування padding
        mask = (target != self.ignore_index).float()
        mask_sum = mask.sum()

        # Якщо всі токени - padding, повертаємо 0
        if mask_sum == 0:
            return torch.tensor(0.0, device=output.device)

        # Кількість класів
        n_class = output.size(1)

        # Перетворюємо цілі у one-hot вектори
        one_hot = torch.zeros_like(output).scatter(1, target.unsqueeze(1), 1)

        # Додаємо згладжування
        one_hot = one_hot * (1.0 - self.smoothing) + self.smoothing / n_class

        # Застосовуємо маску для ігнорування padding
        one_hot = one_hot * mask.unsqueeze(1)

        # Застосовуємо log_softmax до виходу
        log_prob = F.log_softmax(output, dim=1)

        # Обчислюємо втрати
        loss = self.criterion(log_prob, one_hot)

        # Нормалізуємо за кількістю токенів (без padding)
        loss = loss / mask_sum

        return loss

