import os
import torch
import numpy as np
import matplotlib.pyplot as plt
from torchvision import datasets, transforms
from torch.utils.data import DataLoader, Subset
from sklearn.model_selection import train_test_split
import time
import copy
from torchvision import models
import torch.nn as nn
import torch.optim as optim
from sklearn.metrics import classification_report, confusion_matrix
import seaborn as sns
import matplotlib.pyplot as plt

"""**Key Pipeline Engineering Decisions:**
1. **Context-Disrupting Augmentation (Train Transforms):** Integrates RandomResizedCrop and ColorJitter on the training subset to break down static laboratory background frame artifacts, forcing the model filters to isolate structural sheet-metal deformation contours.
2. **Deterministic Evaluation (Test Transforms):** Applies pristine ImageNet standardization tensors with zero spatial distortions to secure completely objective validation tracking.
3. **Rigid Stratification:** Implements a strict stratified split to allocate exactly 17 training images and 3 testing images per class. The resulting 51/9 distribution ensures a perfectly balanced 3 x 3 confusion matrix space.
"""

torch.manual_seed(42)
np.random.seed(42)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"IDE: {device}")

# Training pipeline: strong augmentation to destroy the background context of the MGA stand
train_transform = transforms.Compose([
    transforms.Resize((256, 256)),
    transforms.RandomResizedCrop(224, scale=(0.75, 1.0)),
    transforms.RandomHorizontalFlip(),
    transforms.ColorJitter(brightness=0.2, contrast=0.2),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
])

# Test pipeline: strict normalization without distortion
test_transform = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
])

DATASET_PATH = 'dataset'

# Get labels
base_dataset = datasets.ImageFolder(root=DATASET_PATH)
labels = base_dataset.targets
class_names = base_dataset.classes

# stratified Split
train_idx, test_idx = train_test_split(
    np.arange(len(labels)),
    test_size=9,
    stratify=labels,
    random_state=42
)

# subsets with appropriate transformations
train_dataset = Subset(datasets.ImageFolder(root=DATASET_PATH, transform=train_transform), train_idx)
test_dataset = Subset(datasets.ImageFolder(root=DATASET_PATH, transform=test_transform), test_idx)

#batch packs
train_loader = DataLoader(train_dataset, batch_size=8, shuffle=True)
test_loader = DataLoader(test_dataset, batch_size=9, shuffle=False)

print("\n--- Init report ---")
print(f"Classes: {class_names}")
print(f"Images for Train: {len(train_dataset)}")
print(f"Images for Test: {len(test_dataset)}")
print("Done")

"""
To establish a rigid performance baseline, the pipeline first deploys a lightweight, pre-trained MobileNetV2 architecture. The core convolutional feature extraction layers are frozen to leverage pre-trained ImageNet structural primitives (edges, metallic textures), while the terminal classification head is completely re-engineered to map the latent embeddings directly into our 3 target domains (front, side, rear)."""

# pre-trained MobileNetV2
print("Loading base MobileNetV2...")
model = models.mobilenet_v2(weights=models.MobileNet_V2_Weights.DEFAULT)

# Freeze all layers
for param in model.parameters():
    param.requires_grad = False

# Replace final classifier on 3
num_features = model.classifier[1].in_features
model.classifier = nn.Sequential(
    nn.Dropout(0.2),
    nn.Linear(num_features, len(class_names))
)

model = model.to(device)

criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(model.classifier.parameters(), lr=0.001)

#  training + track best
epochs = 15
train_losses, val_losses = [], []
train_accs, val_accs = [], []

best_model_wts = copy.deepcopy(model.state_dict())
best_acc = 0.0

print("\n Initialize learning")
start_time = time.time()

for epoch in range(epochs):
    print(f'Epoch {epoch+1}/{epochs} | ', end='')

    #  Learning
    model.train()
    running_loss, running_corrects = 0.0, 0

    for inputs, labels in train_loader:
        inputs, labels = inputs.to(device), labels.to(device)

        optimizer.zero_grad()
        outputs = model(inputs)
        _, preds = torch.max(outputs, 1)
        loss = criterion(outputs, labels)

        loss.backward()
        optimizer.step()

        running_loss += loss.item() * inputs.size(0)
        running_corrects += torch.sum(preds == labels.data)

    epoch_loss = running_loss / len(train_dataset)
    epoch_acc = running_corrects.double() / len(train_dataset)
    train_losses.append(epoch_loss)
    train_accs.append(epoch_acc.item())

    #  Valid
    model.eval()
    val_loss, val_corrects = 0.0, 0

    with torch.no_grad():
        for inputs, labels in test_loader:
            inputs, labels = inputs.to(device), labels.to(device)
            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)
            loss = criterion(outputs, labels)

            val_loss += loss.item() * inputs.size(0)
            val_corrects += torch.sum(preds == labels.data)

    val_epoch_loss = val_loss / len(test_dataset)
    val_epoch_acc = val_corrects.double() / len(test_dataset)
    val_losses.append(val_epoch_loss)
    val_accs.append(val_epoch_acc.item())

    print(f'Train Loss: {epoch_loss:.4f}, Acc: {epoch_acc:.4f} | Val Loss: {val_epoch_loss:.4f}, Acc: {val_epoch_acc:.4f}')

    # best model weights
    if val_epoch_acc > best_acc:
        best_acc = val_epoch_acc
        best_model_wts = copy.deepcopy(model.state_dict())

time_elapsed = time.time() - start_time
print(f'\n Learning done: {time_elapsed // 60:.0f}m {time_elapsed % 60:.0f}s')
print(f' Best validation accuracy: {best_acc:.4f}')

model.load_state_dict(best_model_wts)

# Swirching model to test
model.eval()
all_preds = []
all_targets = []

with torch.no_grad():
    for inputs, labels in test_loader:
        inputs = inputs.to(device)
        outputs = model(inputs)
        _, preds = torch.max(outputs, 1)

        all_preds.extend(preds.cpu().numpy())
        all_targets.extend(labels.numpy())

print("\n--- CLASSIFICATION REPORT ---")
print(classification_report(all_targets, all_preds, target_names=class_names))
cm = confusion_matrix(all_targets, all_preds)
plt.figure(figsize=(7, 5))
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=class_names, yticklabels=class_names)
plt.title('Confusion Matrix - Validation Data')
plt.ylabel('True Class')
plt.xlabel('Predicted Class')
plt.show()

model.eval()

# Retrieve the complete validation batch from the non-shuffled test_loader
images_batch, labels_batch = next(iter(test_loader))

# Forward pass tensors through the active compute device
inputs = images_batch.to(device)
with torch.no_grad():
    outputs = model(inputs)
    _, preds = torch.max(outputs, 1)

images_batch = images_batch.cpu()
labels_batch = labels_batch.cpu().numpy()
preds = preds.cpu().numpy()


plt.figure(figsize=(12, 12))

for i in range(len(images_batch)):
    ax = plt.subplot(3, 3, i + 1)

    # De-normalize ImageNet metrics back to standard [0, 1] RGB range for visualization
    img = images_batch[i].permute(1, 2, 0).numpy()
    mean = np.array([0.485, 0.456, 0.406])
    std = np.array([0.229, 0.224, 0.225])
    img = std * img + mean
    img = np.clip(img, 0, 1)

    plt.imshow(img)

    # Check prediction accuracy to determine title text colors
    is_correct = (preds[i] == labels_batch[i])
    title_color = 'darkgreen' if is_correct else 'crimson'

    title_text = f"True: {class_names[labels_batch[i]].upper()}\nPred: {class_names[preds[i]].upper()}"
    if not is_correct:
        title_text += " [MISMATCH]"

    plt.title(title_text, color=title_color, fontsize=11, fontweight='bold')
    plt.axis('off')

plt.tight_layout()
plt.show()

"""The baseline MobileNetV2 network reached its performance ceiling at a peak validation accuracy of **77.78%**, with the validation loss plateauing at **0.6142** by Epoch 13.

**Key Technical Findings:**
* **Elevated Loss Floor:** The validation loss remained relatively high, indicating that while the network successfully mapped clear-cut directional indicators (e.g., severe airbag deployment in frontal crashes), it struggled with geometric ambiguities where side-impact warping and trailing-edge rear deformations overlap.
* **Feature Capacity Limits:** MobileNetV2 is optimized primarily for lightweight efficiency. This architectural constraint limits its parameter density, preventing it from deeply resolving complex, non-linear steel crumple patterns and subtle 2D deformation contours.

*R&D Note: At this stage, hyperparameter optimization or selective block fine-tuning could potentially resolve these boundary ambiguities while preserving the model's lightweight, edge-ready footprint. However, a structural pivot to a more complex compound-scaled architecture (EfficientNet-B0) was executed first. Because this architectural upgrade immediately yielded optimal training and validation convergence, further parameter tuning on the baseline was bypassed. Nevertheless, optimizing the baseline parameters remains a highly viable and preferred roadmap alternative for deployment environments with strict hardware and compute constraints.*

**Architectural Pivot:** To eliminate these boundary ambiguities and build a highly robust routing layer, the pipeline moves away from lightweight mobile backbones and transitions to a heavier, compound-scaled architecture: **EfficientNet-B0**.
"""

