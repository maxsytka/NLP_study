def evaluate_stylistic_alignment(original_texts, generated_texts):
    """Evaluate stylistic alignment between original and generated texts"""
    # Average sentence length
    def avg_sentence_length(text):
        sentences = nltk.sent_tokenize(text)
        if not sentences:
            return 0
        return sum(len(nltk.word_tokenize(sentence)) for sentence in sentences) / len(sentences)

    original_avg_sent_len = [avg_sentence_length(text) for text in original_texts]
    generated_avg_sent_len = [avg_sentence_length(text) for text in generated_texts]

    sent_len_similarity = 1 - abs(np.mean(original_avg_sent_len) - np.mean(generated_avg_sent_len)) / max(np.mean(original_avg_sent_len), 1)

    # Vocabulary overlap
    def vocab_overlap(text1, text2):
        words1 = set(nltk.word_tokenize(text1.lower()))
        words2 = set(nltk.word_tokenize(text2.lower()))
        intersection = words1.intersection(words2)
        union = words1.union(words2)
        return len(intersection) / len(union) if union else 0

    vocab_similarities = [vocab_overlap(orig, gen) for orig, gen in zip(original_texts, generated_texts)]
    avg_vocab_similarity = np.mean(vocab_similarities)

    # Return composite score
    return (sent_len_similarity + avg_vocab_similarity) / 2

