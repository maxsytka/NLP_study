def prepare_dataset(input_file, output_file=None, min_length=50):
    """Prepare dataset for fine-tuning"""
    if output_file is None:
        output_file = f"{input_file.rsplit('.', 1)[0]}_prepared.txt"

    # Read the file
    with open(input_file, 'r', encoding='utf-8') as f:
        content = f.read()

    # Split into sections
    sections = re.split(r'\n\s*\n', content)
    filtered_sections = [section.strip() for section in sections if len(section.strip()) >= min_length]

    print(f"Found {len(filtered_sections)} sections (min length: {min_length})")

    # Write prepared dataset
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write("\n\n".join(filtered_sections))

    print(f"Prepared dataset saved to {output_file}")
    return output_file

