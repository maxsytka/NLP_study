# Крок 4: Попередня обробка тексту
def preprocess_text(text):
    """
    Покращена попередня обробка тексту: приведення до нижнього регістру, видалення спеціальних символів,
    видалення стоп-слів, стемінг та додаткова фільтрація.

    Аргументи:
        text: Вхідний текст

    Повертає:
        Список токенів після обробки
    """
    # Приведення до нижнього регістру
    text = text.lower()

    # Видалення спеціальних символів і цифр
    text = re.sub(r'[^a-zA-Z\s]', '', text)

    # Токенізація
    tokens = word_tokenize(text)

    # Видалення стоп-слів
    stop_words = set(stopwords.words('english'))
    tokens = [token for token in tokens if token not in stop_words]

    # Стемінг
    stemmer = PorterStemmer()
    tokens = [stemmer.stem(token) for token in tokens]

    # Додаткова фільтрація
    # 1. Видалення токенів довжиною <= 2 або > 20
    # 2. Видалення повторюваних паттернів (як "maxaxaxaxa...")
    filtered_tokens = []
    for token in tokens:
        # Перевірка довжини
        if len(token) <= 2 or len(token) > 20:
            continue

        # Перевірка на повторювані патерни
        # Перевіряємо, чи містить слово повторюваний патерн довжиною 2+ символи
        has_repeating_pattern = False
        for pattern_len in range(2, len(token)//2 + 1):
            pattern = token[:pattern_len]
            if token == pattern * (len(token)//pattern_len) + token[:(len(token) % pattern_len)]:
                has_repeating_pattern = True
                break

        if not has_repeating_pattern:
            filtered_tokens.append(token)

    return filtered_tokens

