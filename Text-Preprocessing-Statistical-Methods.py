# Крок 8: Реалізація Word2Vec з використанням PyTorch
class Word2VecDataset(Dataset):
    """
    Датасет для тренування Word2Vec.
    """
    def __init__(self, texts, word2idx, context_size=2, mode='cbow'):
        """
        Ініціалізація датасету.

        Аргументи:
            texts: Список токенізованих текстів
            word2idx: Словник відображення слів в індекси
            context_size: Розмір контекстного вікна (кількість слів з кожного боку)
            mode: 'cbow' або 'skipgram'
        """
        self.texts = texts
        self.word2idx = word2idx
        self.context_size = context_size
        self.mode = mode
        self.data = self._prepare_data()

    def _prepare_data(self):
        """
        Підготовка даних для тренування Word2Vec.

        Повертає:
            Список пар (контекст, ціль)
        """
        data = []

        for tokens in self.texts:
            # Фільтрація токенів у словнику
            word_indices = [self.word2idx[token] for token in tokens if token in self.word2idx]

            # Генерація пар контекст-ціль
            for i in range(len(word_indices)):
                target_idx = word_indices[i]

                # Визначення контекстного вікна
                context_indices = []
                for j in range(max(0, i - self.context_size), min(len(word_indices), i + self.context_size + 1)):
                    if i != j:  # Пропуск цільового слова
                        context_indices.append(word_indices[j])

                # Пропуск, якщо контекст порожній
                if not context_indices:
                    continue

                if self.mode == 'cbow':
                    # CBOW: контекст → ціль
                    data.append((context_indices, target_idx))
                else:  # skipgram
                    # Skip-gram: ціль → кожне слово контексту
                    for context_idx in context_indices:
                        data.append((target_idx, context_idx))

        return data

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        context, target = self.data[idx]

        if self.mode == 'cbow':
            # Перетворення контексту в тензор
            context_vector = torch.zeros(len(self.word2idx))
            for context_idx in context:
                context_vector[context_idx] = 1.0 / len(context)
            return context_vector, torch.tensor(target, dtype=torch.long)
        else:  # skipgram
            return torch.tensor(context, dtype=torch.long), torch.tensor(target, dtype=torch.long)

class CBOWModel(nn.Module):
    """
    Модель Continuous Bag of Words (CBOW) з використанням PyTorch.
    """
    def __init__(self, vocab_size, embedding_dim):
        """
        Ініціалізація моделі.

        Аргументи:
            vocab_size: Розмір словника
            embedding_dim: Розмірність вбудувань слів
        """
        super(CBOWModel, self).__init__()
        self.embeddings = nn.Linear(vocab_size, embedding_dim, bias=False)
        self.output = nn.Linear(embedding_dim, vocab_size, bias=False)

    def forward(self, x):
        """
        Прямий прохід.

        Аргументи:
            x: Вхідний вектор контексту

        Повертає:
            Вихідні логіти
        """
        embedded = self.embeddings(x)
        out = self.output(embedded)
        return out

class SkipGramModel(nn.Module):
    """
    Модель Skip-gram з використанням PyTorch.
    """
    def __init__(self, vocab_size, embedding_dim):
        """
        Ініціалізація моделі.

        Аргументи:
            vocab_size: Розмір словника
            embedding_dim: Розмірність вбудувань слів
        """
        super(SkipGramModel, self).__init__()
        self.embeddings = nn.Embedding(vocab_size, embedding_dim)
        self.output = nn.Linear(embedding_dim, vocab_size)

    def forward(self, x):
        """
        Прямий прохід.

        Аргументи:
            x: Індекс вхідного слова

        Повертає:
            Вихідні логіти
        """
        embedded = self.embeddings(x)
        out = self.output(embedded)
        return out

def train_word2vec(texts, word2idx, embedding_dim=100, mode='cbow', context_size=2, epochs=5, batch_size=64):
    """
    Тренування моделі Word2Vec.

    Аргументи:
        texts: Список токенізованих текстів
        word2idx: Словник відображення слів в індекси
        embedding_dim: Розмірність вбудувань слів
        mode: 'cbow' або 'skipgram'
        context_size: Розмір контекстного вікна
        epochs: Кількість епох тренування
        batch_size: Розмір пакету

    Повертає:
        Матрицю вбудувань слів
    """
    vocab_size = len(word2idx)

    # Створення датасету і завантажувача даних
    dataset = Word2VecDataset(texts, word2idx, context_size=context_size, mode=mode)
    dataloader = DataLoader(dataset, batch_size=batch_size, shuffle=True)

    # Ініціалізація моделі
    if mode == 'cbow':
        model = CBOWModel(vocab_size, embedding_dim)
    else:  # skipgram
        model = SkipGramModel(vocab_size, embedding_dim)

    # Визначення функції втрат і оптимізатора
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=0.001)

    # Цикл тренування
    for epoch in range(epochs):
        total_loss = 0
        for context, target in tqdm(dataloader, desc=f"Епоха {epoch+1}/{epochs}"):
            # Прямий прохід
            if mode == 'cbow':
                output = model(context)
            else:  # skipgram
                output = model(context)

            # Обчислення втрат
            loss = criterion(output, target)

            # Зворотний прохід та оптимізація
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            total_loss += loss.item()

        avg_loss = total_loss / len(dataloader)
        print(f"Епоха {epoch+1}/{epochs}, Втрати: {avg_loss:.4f}")

    # Отримання вбудувань слів
    if mode == 'cbow':
        # Для CBOW моделі, як вона реалізована, нам потрібно перевірити форму
        raw_embeddings = model.embeddings.weight.data.numpy()
        print(f"Початкова форма вбудувань: {raw_embeddings.shape}")

        # Перевірка та корекція форми
        if raw_embeddings.shape[0] != len(word2idx):
            embeddings = raw_embeddings.T
        else:
            embeddings = raw_embeddings

        print(f"Кінцева форма вбудувань: {embeddings.shape}")
    else:  # skipgram
        embeddings = model.embeddings.weight.data.numpy()

    return embeddings, model

