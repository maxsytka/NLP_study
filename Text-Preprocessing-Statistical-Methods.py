# Крок 7: Реалізація TF-IDF
def tfidf_transform(texts, word2idx):
    """
    Перетворення текстів у TF-IDF вектори за допомогою NumPy.

    Аргументи:
        texts: Список токенізованих текстів
        word2idx: Словник відображення слів в індекси

    Повертає:
        TF-IDF вектори для кожного тексту
    """
    vocab_size = len(word2idx)
    num_docs = len(texts)

    # Обчислення частоти термінів (TF)
    tf = np.zeros((num_docs, vocab_size), dtype=np.float32)
    for i, tokens in enumerate(texts):
        # Підрахунок кількості слів у документі
        doc_word_counts = {}
        for token in tokens:
            if token in word2idx:
                doc_word_counts[word2idx[token]] = doc_word_counts.get(word2idx[token], 0) + 1

        # Обчислення TF для кожного слова
        doc_length = len(tokens)
        for word_idx, count in doc_word_counts.items():
            tf[i, word_idx] = count / doc_length

    # Обчислення частоти документів (DF) і оберненої частоти документів (IDF)
    df = np.zeros(vocab_size, dtype=np.float32)
    for tokens in texts:
        # Отримання унікальних слів у документі
        unique_words = set(tokens) & set(word2idx.keys())
        for word in unique_words:
            df[word2idx[word]] += 1

    # Обчислення IDF з згладжуванням
    idf = np.log(num_docs / (df + 1)) + 1

    # Обчислення TF-IDF
    tfidf = tf * idf

    return tfidf

