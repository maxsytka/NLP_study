# Крок 13: Функція для пошуку подібних слів (на основі вбудувань Word2Vec)
def find_similar_words(word, embeddings, word2idx, idx2word, top_n=10):
    """
    Знаходження слів, найбільш подібних до заданого слова.

    Аргументи:
        word: Цільове слово
        embeddings: Матриця вбудувань слів
        word2idx: Словник відображення слів в індекси
        idx2word: Словник відображення індексів в слова
        top_n: Кількість подібних слів для повернення

    Повертає:
        Список кортежів (слово, подібність)
    """
    if word not in word2idx:
        return []

    word_idx = word2idx[word]
    word_embedding = embeddings[word_idx]

    # Обчислення косинусної подібності між цільовим словом і всіма іншими словами
    similarities = np.zeros(len(word2idx))
    for idx in range(len(word2idx)):
        if idx != word_idx:
            similarities[idx] = np.dot(word_embedding, embeddings[idx]) / (
                np.linalg.norm(word_embedding) * np.linalg.norm(embeddings[idx]))

    # Отримання індексів найбільш подібних слів
    most_similar_indices = similarities.argsort()[-top_n-1:-1][::-1]

    # Створення списку кортежів (слово, подібність)
    most_similar = [(idx2word[idx], similarities[idx]) for idx in most_similar_indices]

    return most_similar

