# Крок 8: Функція для пошуку схожих книг з позитивними відгуками
def find_similar_books_with_positive_sentiment(recommender, sentiment_model, sentiment_vectorizer,
                                              item_idx, n=5, similarity_weight=0.7):
    """
    Знаходження схожих книг з позитивними відгуками.

    Аргументи:
        recommender: Об'єкт рекомендера
        sentiment_model: Модель аналізу настроїв
        sentiment_vectorizer: TF-IDF векторизатор для моделі настроїв
        item_idx: Індекс елемента запиту
        n: Кількість рекомендацій
        similarity_weight: Вага схожості (vs вага настрою) при ранжуванні

    Повертає:
        DataFrame з рекомендованими елементами
    """
    # Отримуємо більше подібних елементів для подальшої фільтрації
    similar_items = recommender.get_similar_items(item_idx, n=n*3)

    # Прогнозуємо настрої для відгуків схожих елементів
    sentiments = predict_sentiments(sentiment_model, sentiment_vectorizer, similar_items['review'].tolist())

    # Додаємо настрої до DataFrame
    similar_items['sentiment'] = sentiments
    similar_items['sentiment_score'] = sentiments.astype(float)

    # Обчислюємо комбінований скор (схожість + настрій)
    similar_items['combined_score'] = similarity_weight * similar_items['similarity_score'] + \
                                     (1 - similarity_weight) * similar_items['sentiment_score']

    # Сортуємо за комбінованим скором
    similar_items = similar_items.sort_values('combined_score', ascending=False)

    # Повертаємо топ-N результатів
    return similar_items.head(n)

