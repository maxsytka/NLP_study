# Крок 6: Реалізація Bag of Words (BoW)
def bow_transform(texts, word2idx):
    """
    Перетворення текстів у вектори Bag of Words за допомогою NumPy.

    Аргументи:
        texts: Список токенізованих текстів
        word2idx: Словник відображення слів в індекси

    Повертає:
        BoW вектори для кожного тексту
    """
    vocab_size = len(word2idx)
    bow_vectors = np.zeros((len(texts), vocab_size), dtype=np.int32)

    for i, tokens in enumerate(texts):
        for token in tokens:
            if token in word2idx:
                bow_vectors[i, word2idx[token]] += 1

    return bow_vectors

