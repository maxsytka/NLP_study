# Крок 9: Підготовка вбудувань для класифікації
def document_to_vector(tokens, embeddings, word2idx, embedding_dim):
    """
    Перетворення документа у вектор вбудування шляхом усереднення вбудувань слів.

    Аргументи:
        tokens: Список токенів
        embeddings: Матриця вбудувань слів
        word2idx: Словник відображення слів в індекси
        embedding_dim: Розмірність вбудувань

    Повертає:
        Вектор документа
    """
    vector = np.zeros(embedding_dim)
    count = 0

    for token in tokens:
        if token in word2idx:
            vector += embeddings[word2idx[token]]
            count += 1

    if count > 0:
        vector /= count

    return vector

