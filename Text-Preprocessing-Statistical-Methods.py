def clean_text(text):
    """
    Надійна функція очищення тексту без зовнішніх залежностей.

    Аргументи:
        text: Вхідний текст

    Повертає:
        Очищений текст
    """
    # Обробка значень NaN
    if pd.isna(text):
        return ""

    # Приведення до нижнього регістру
    text = text.lower()

    # Видалення спеціальних символів і цифр
    text = re.sub(r'[^a-zA-Z\s]', ' ', text)

    # Видалення зайвих пробілів
    text = re.sub(r'\s+', ' ', text).strip()

    return text

def simple_tokenize(text):
    """
    Проста токенізація тексту без зовнішніх залежностей.

    Аргументи:
        text: Вхідний текст

    Повертає:
        Список токенів
    """
    # Очищення тексту
    clean = clean_text(text)

    # Розбиття на токени за пробілами
    tokens = clean.split()

    # Фільтрація коротких слів
    tokens = [token for token in tokens if len(token) > 2]

    return tokens

