def build_vocabulary(texts, min_count=5, max_count=None):
    """
    Побудова словника зі списку токенізованих текстів з додатковими фільтрами.

    Аргументи:
        texts: Список токенізованих текстів
        min_count: Мінімальна частота слова для включення в словник
        max_count: Максимальна частота слова (щоб уникнути надто частих слів)

    Повертає:
        word2idx, idx2word, word_counts
    """
    # Підрахунок частот слів
    word_counts = Counter()
    for tokens in texts:
        word_counts.update(tokens)

    # Фільтрація слів за частотою
    filtered_words = []
    for word, count in word_counts.items():
        # Перевірка мінімальної частоти
        if count < min_count:
            continue

        # Перевірка максимальної частоти (якщо вказано)
        if max_count is not None and count > max_count:
            continue

        filtered_words.append(word)

    # Створення відображень слово-індекс та індекс-слово
    word2idx = {word: i for i, word in enumerate(filtered_words)}
    idx2word = {i: word for word, i in word2idx.items()}

    return word2idx, idx2word, word_counts

