# Крок 5: Реалізація рекомендаційної системи на основі вмісту
class ContentBasedRecommender:
    """
    Рекомендаційна система на основі вмісту з використанням TF-IDF або Sentence Transformers.
    """
    def __init__(self, embedding_method='tfidf'):
        """
        Ініціалізація рекомендера.

        Аргументи:
            embedding_method: 'tfidf' або 'sbert'
        """
        self.embedding_method = embedding_method

        if embedding_method == 'tfidf':
            self.vectorizer = TfidfVectorizer(max_features=5000)
        elif embedding_method == 'sbert':
            self.model = SentenceTransformer('all-MiniLM-L6-v2')
        else:
            raise ValueError("Метод вбудування має бути 'tfidf' або 'sbert'")

        self.item_embeddings = None
        self.items_df = None

    def fit(self, items_df, text_column='description'):
        """
        Навчання рекомендера на описах елементів.

        Аргументи:
            items_df: DataFrame з елементами
            text_column: Стовпець, що містить текст для вбудування
        """
        self.items_df = items_df.copy()

        # Попередня обробка тексту
        print("Попередня обробка тексту...")
        self.items_df['processed_text'] = self.items_df[text_column].apply(preprocess_text)

        # Перевірка результатів обробки
        non_empty = (self.items_df['processed_text'] != '').sum()
        print(f"Кількість непорожніх описів після обробки: {non_empty} з {len(self.items_df)}")

        # Генерація вбудувань
        print(f"Генерація вбудувань {self.embedding_method}...")
        if self.embedding_method == 'tfidf':
            self.item_embeddings = self.vectorizer.fit_transform(self.items_df['processed_text'])
        else:  # sbert
            # Конвертація у список для SBERT
            texts = self.items_df['processed_text'].tolist()
            self.item_embeddings = self.model.encode(texts, show_progress_bar=True)

        print(f"Згенеровано вбудування з формою: {self.item_embeddings.shape}")

    def get_similar_items(self, item_idx, n=5):
        """
        Отримання найбільш подібних елементів до заданого елемента.

        Аргументи:
            item_idx: Індекс елемента запиту
            n: Кількість подібних елементів для повернення

        Повертає:
            DataFrame з подібними елементами
        """
        if self.embedding_method == 'tfidf':
            # Отримання вбудування для елемента запиту
            query_embedding = self.item_embeddings[item_idx]

            # Обчислення косинусної подібності
            similarities = cosine_similarity(query_embedding, self.item_embeddings).flatten()
        else:  # sbert
            # Отримання вбудування для елемента запиту
            query_embedding = self.item_embeddings[item_idx].reshape(1, -1)

            # Обчислення косинусної подібності
            similarities = cosine_similarity(query_embedding, self.item_embeddings).flatten()

        # Отримання індексів найбільш подібних елементів (за винятком елемента запиту)
        similar_indices = similarities.argsort()[:-n-2:-1]
        similar_indices = [idx for idx in similar_indices if idx != item_idx][:n]

        # Отримання оцінок подібності
        similar_scores = [similarities[idx] for idx in similar_indices]

        # Створення DataFrame з подібними елементами
        similar_items = self.items_df.iloc[similar_indices].copy()
        similar_items['similarity_score'] = similar_scores

        return similar_items

    def recommend_for_item(self, item_idx, n=5, min_rating=4):
        """
        Рекомендація елементів, подібних до заданого елемента з високими рейтингами.

        Аргументи:
            item_idx: Індекс елемента запиту
            n: Кількість рекомендацій для повернення
            min_rating: Мінімальний поріг рейтингу

        Повертає:
            DataFrame з рекомендованими елементами
        """
        # Отримання подібних елементів
        similar_items = self.get_similar_items(item_idx, n=n*2)  # Отримання більшої кількості для фільтрації за рейтингом

        # Фільтрація за рейтингом
        recommended_items = similar_items[similar_items['rating'] >= min_rating].head(n)

        return recommended_items

# Крок 6: Створення спрощеної моделі аналізу настроїв
def create_sentiment_analyzer(reviews, ratings, test_reviews=None, test_ratings=None):
    """
    Створення моделі аналізу настроїв з використанням TF-IDF та LogisticRegression.

    Аргументи:
        reviews: Список текстів відгуків
        ratings: Список рейтингів
        test_reviews: Список текстів тестових відгуків
        test_ratings: Список тестових рейтингів

    Повертає:
        sentiment_model: Навчена модель аналізу настроїв
        vectorizer: TF-IDF векторизатор
    """
    print("\nСтворення моделі аналізу настроїв...")

    # Попередня обробка текстів
    print("Попередня обробка відгуків...")
    processed_reviews = [clean_text(review) for review in reviews]

    # Перевірка даних
    print(f"Кількість відгуків: {len(processed_reviews)}")
    review_lengths = [len(review) for review in processed_reviews]
    print(f"Середня довжина відгуку: {sum(review_lengths)/len(review_lengths):.2f} символів")

    # Перетворення рейтингів у бінарні мітки (1 для ≥4, 0 для <4)
    binary_ratings = [1 if rating >= 4 else 0 for rating in ratings]
    positive_ratio = sum(binary_ratings)/len(binary_ratings)
    print(f"Відсоток позитивних відгуків: {positive_ratio:.2f}")

    # Створення TF-IDF векторизатора
    vectorizer = TfidfVectorizer(max_features=5000, min_df=5)
    X_train = vectorizer.fit_transform(processed_reviews)
    print(f"Розмір словника TF-IDF: {len(vectorizer.get_feature_names_out())}")
    print(f"Форма матриці ознак: {X_train.shape}")

    # Навчання моделі
    print("Навчання моделі логістичної регресії...")
    model = LogisticRegression(max_iter=1000, random_state=42)
    model.fit(X_train, binary_ratings)

    # Оцінка на тренувальному наборі
    train_accuracy = model.score(X_train, binary_ratings)
    print(f"Точність на тренувальному наборі: {train_accuracy:.4f}")

    # Оцінка на тестовому наборі, якщо наданий
    if test_reviews is not None and test_ratings is not None:
        processed_test_reviews = [clean_text(review) for review in test_reviews]
        binary_test_ratings = [1 if rating >= 4 else 0 for rating in test_ratings]

        try:
            X_test = vectorizer.transform(processed_test_reviews)
            test_accuracy = model.score(X_test, binary_test_ratings)
            print(f"Точність на тестовому наборі: {test_accuracy:.4f}")
        except Exception as e:
            print(f"Помилка при оцінці на тестовому наборі: {e}")

    return model, vectorizer

def predict_sentiments(model, vectorizer, reviews):
    """
    Прогнозування настроїв для відгуків.

    Аргументи:
        model: Навчена модель аналізу настроїв
        vectorizer: TF-IDF векторизатор
        reviews: Список текстів відгуків

    Повертає:
        Масив прогнозів (1 для позитивного, 0 для негативного)
    """
    # Попередня обробка
    processed_reviews = [clean_text(review) for review in reviews]

    # Перетворення в TF-IDF
    X = vectorizer.transform(processed_reviews)

    # Прогнозування
    predictions = model.predict(X)

    return predictions

